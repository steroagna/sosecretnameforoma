public class Exam {
    private int id;
    private int studentsEnrolled;

    public Exam(int id, int studentsEnrolled) {
        this.id = id;
        this.studentsEnrolled = studentsEnrolled;
    }

    public int getId() {
        return id;
    }

    public int getStudentsEnrolled() {
        return studentsEnrolled;
    }
}
